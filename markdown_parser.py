def calculate_summary_matrix(data: Dict[str, Any]) -> Dict[str, Any]:
    """
    Calculate the 8-category × 9-province summary matrix.
    Used by web_report_engine.py for institutional report generation.
    
    Args:
        data: Parsed report data dictionary
        
    Returns:
        Dictionary with matrix data for report rendering
    """
    sections = data.get("sections", []) if isinstance(data, dict) else data
    
    matrix = {
        "headers": SUMMARY_CATEGORIES,
        "rows": [],
        "totals": [0] * len(SUMMARY_CATEGORIES),
        "grand_total_all": 0
    }
    
    for p_base in OFFICIAL_PROVINCES:
        row = {
            "province": f"{p_base.title()} Province",
            "values": [0] * len(SUMMARY_CATEGORIES),
            "grand_total": 0
        }
        
        for sec in sections:
            s_title = sec["title"].upper()
            for prov in sec["provinces"]:
                # Case-insensitive province matching
                if p_base in prov["name"].upper():
                    for inc in prov["incidents"]:
                        txt = inc.get("body", "").upper()
                        idx = _categorize_incident(inc.get("body", ""), sec["title"])
                        
                        row["values"][idx] += 1
                        row["grand_total"] += 1
                        matrix["totals"][idx] += 1
                        matrix["grand_total_all"] += 1
        
        # Format values: show count if >0, otherwise "-"
        row["values"] = [f"{v:02d}" if v > 0 else "-" for v in row["values"]]
        matrix["rows"].append(row)
    
    # Format totals
    matrix["totals"] = [f"{v:02d}" if v > 0 else "-" for v in matrix["totals"]]
    
    return matrix


def _calculate_case_data(sections: List[Dict[str, Any]]) -> List[Dict[str, str]]:
    """
    Calculate the 28-row official case breakdown using incident-level mapping.
    Matches the official Sri Lanka Police summary table format.
    
    Args:
        sections: List of parsed section dictionaries
        
    Returns:
        List of 28 dictionaries with reported/solved/unsolved counts
    """
    # Initialize 28 rows with default values
    rows = [
        {"reported_int": 0, "solved_int": 0, "reported": "-", "solved": "-", "unsolved": "-"}
        for _ in range(28)
    ]
    
    for sec in sections:
        for prov in sec["provinces"]:
            for inc in prov["incidents"]:
                body = inc.get("body", "")
                
                # Map to case row index (0-27)
                idx = _map_to_case_row(body)
                
                # Increment reported count
                rows[idx]["reported_int"] += 1
                
                # Check if incident appears solved
                if _is_solved_incident(body):
                    rows[idx]["solved_int"] += 1
    
    # Format integers to strings for report engine
    for row in rows:
        if row["reported_int"] > 0:
            row["reported"] = f"{row['reported_int']:02d}"
        if row["solved_int"] > 0:
            row["solved"] = f"{row['solved_int']:02d}"
        
        unsolved = row["reported_int"] - row["solved_int"]
        if unsolved > 0:
            row["unsolved"] = f"{unsolved:02d}"
        
        # Remove temporary integer fields
        del row["reported_int"]
        del row["solved_int"]
    
    return rows


