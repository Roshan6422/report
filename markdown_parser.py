def _ordinal_day(day: int) -> str:
    """Convert day number to ordinal string (1 → '1st', 2 → '2nd', etc.)."""
    if 11 <= day <= 13:
        return f"{day}th"
    return f"{day}{['th', 'st', 'nd', 'rd', 'th', 'th', 'th', 'th', 'th', 'th'][day % 10]}"


def _parse_date_range(text: str) -> Optional[str]:
    """
    Extract and format date range from markdown text.
    Supports multiple formats: DD-MM-YYYY, DD/MM/YYYY, DD.MM.YYYY, etc.
    """
    if not text:
        return None
    
    # Try multiple date patterns
    patterns = [
        r'(\d{1,2})[-/\.](\d{1,2})[-/\.](\d{4})',  # DD-MM-YYYY, DD/MM/YYYY, etc.
        r'(\d{4})[-/\.](\d{1,2})[-/\.](\d{1,2})',  # YYYY-MM-DD
        r'(\d{1,2}[a-z]{2}\s+\w+\s+\d{4})',        # 18th March 2026
    ]
    
    for pattern in patterns:
        match = re.search(pattern, text[:500], re.IGNORECASE)
        if match:
            try:
                groups = match.groups()
                if len(groups) == 3 and groups[0].isdigit():
                    # DD-MM-YYYY or YYYY-MM-DD format
                    if len(groups[2]) == 4:  # Likely DD-MM-YYYY
                        day, month, year = int(groups[0]), int(groups[1]), int(groups[2])
                    else:  # Likely YYYY-MM-DD
                        year, month, day = int(groups[0]), int(groups[1]), int(groups[2])
                    
                    if 1 <= day <= 31 and 1 <= month <= 12:
                        d2 = datetime(year, month, day)
                        d1 = d2 - timedelta(days=1)
                        return f"From 0400 hrs. on {_ordinal_day(d1.day)} of {d1.strftime('%B %Y')} to 0400 hrs. on {_ordinal_day(d2.day)} of {d2.strftime('%B %Y')}"
                elif len(groups) == 1:
                    # Already in ordinal format like "18th March 2026"
                    return f"From 0400 hrs. on {groups[0]} to 0400 hrs. on {_ordinal_day((datetime.strptime(groups[0].split()[0].rstrip('stndrdth'), '%d') + timedelta(days=1)).day)} of {groups[0].split()[1]} {groups[0].split()[2]}"
            except (ValueError, IndexError, AttributeError):
                continue
    
    return None


def _clean_sl_chars(txt: Optional[str]) -> str:
    """
    Strip Sinhala (U+0D80-0DFF) and Tamil (U+0B80-0BFF) script characters,
    but only if the text is predominantly Latin (English).
    Preserves non-Latin text if it's the main content.
    """
    if not txt:
        return ""
    
    txt = txt.strip()
    if not txt:
        return ""
    
    # Count Latin vs non-Latin characters
    latin_chars = len(re.findall(r'[a-zA-Z]', txt))
    total_alpha = len(re.findall(r'\w', txt))
    
    # If text is mostly non-Latin (<30% Latin), return as-is to avoid data destruction
    if total_alpha > 0 and latin_chars / total_alpha < 0.3:
        return txt
    
    # Strip Sinhala and Tamil script characters
    cleaned = re.sub(r'[\u0D80-\u0DFF\u0B80-\u0BFF]+', ' ', txt)
    # Clean up extra spaces
    cleaned = re.sub(r'\s+', ' ', cleaned).strip()
    
    return cleaned


def _normalize_province(name: str) -> str:
    """Normalize province name to official canonical form."""
    if not name:
        return "UNKNOWN"
    
    name = name.strip().upper()
    
    # Handle common variations and abbreviations
    variants = {
        "N. WESTERN": "NORTH WESTERN", "WAYAMBA": "NORTH WESTERN",
        "N. CENTRAL": "NORTH CENTRAL", "RAJARATA": "NORTH CENTRAL",
        "SABARA": "SABARAGAMUWA", "SOUTH": "SOUTHERN",
        "NORTH": "NORTHERN", "EAST": "EASTERN", "WEST": "WESTERN",
        "NORTH-WESTERN": "NORTH WESTERN", "NORTHWESTERN": "NORTH WESTERN",
        "NORTH-CENTRAL": "NORTH CENTRAL", "NORTHCENTRAL": "NORTH CENTRAL",
        "UVA PROVINCE": "UVA", "CENTRAL PROVINCE": "CENTRAL",
    }
    
    normalized = variants.get(name, name)
    
    # Ensure "PROVINCE" suffix for official format
    if "PROVINCE" not in normalized and normalized in OFFICIAL_PROVINCES:
        normalized = f"{normalized} PROVINCE"
    
    return normalized


def _extract_hierarchy(hierarchy_str: str) -> Tuple[str, str, str]:
    """
    Parse hierarchy string into S/DIG, DIG, and Division components.
    Returns: (s_dig, dig, division)
    """
    if not hierarchy_str:
        return "NATIONAL", "", ""
    
    s_dig = "NATIONAL"
    dig = ""
    division = ""
    
    parts = [p.strip() for p in hierarchy_str.split(',') if p.strip()]
    
    for part in parts:
        part_upper = part.upper()
        
        if "S/DIG" in part_upper or "SENIOR DIG" in part_upper:
            s_dig = re.sub(r'S/DIG|SENIOR\s*DIG', '', part, flags=re.IGNORECASE).strip()
            s_dig = re.sub(r'Province', '', s_dig, flags=re.IGNORECASE).strip().upper()
        elif "DIG" in part_upper and "S/DIG" not in part_upper:
            dig = part.strip()
        elif any(x in part_upper for x in ["DIV", "DISTRICT", "DIVISION"]):
            division = part.strip()
    
    return s_dig, dig, division


def _is_ai_filler(text: str) -> bool:
    """Check if text contains AI-generated filler content."""
    if not text:
        return True
    
    text_lower = text.lower()
    
    # Check for filler phrases
    if any(phrase in text_lower for phrase in AI_FILLER_PHRASES):
        return True
    
    # Check for very short content (likely placeholder)
    if len(text.strip()) < 20:
        return True
    
    # Check for "nil" or empty indicators
    if re.search(r'\bnil\b|\bnone\b|\b-\b', text_lower):
        return True
    
    return False


def _extract_reference(text: str) -> str:
    """Extract reference code (CTM/OTM/IR) from text."""
    if not text:
        return ""
    
    # Match patterns like: CTM.370, OTM 1136, IR-123, CTM 370MS
    match = re.search(r'\(?([A-Z]{3,4})[\.\s\-]?(\d+)(MS)?\)?', text, re.IGNORECASE)
    if match:
        prefix, number, suffix = match.groups()
        result = f"{prefix}.{number}"
        if suffix:
            result += suffix
        return result
    
    return ""


def _categorize_incident(body: str, section_title: str) -> int:
    """
    Map incident to summary category index (0-7).
    Returns index into SUMMARY_CATEGORIES list.
    """
    if not body:
        return 7  # Others
    
    body_upper = body.upper()
    title_upper = section_title.upper()
    
    # Explicit keyword matching (order matters - more specific first)
    if "HB & THEFT" in body_upper or "HOUSE BREAK" in body_upper or "BURGLARY" in body_upper:
        return 1  # HB & Theft
    elif "VEHICLE" in body_upper and "THEFT" in body_upper:
        return 0  # Theft (vehicle theft is still theft category for summary)
    elif "THEFT" in body_upper:
        return 0  # Theft
    elif "ROBBER" in body_upper or "SNATCH" in body_upper:
        return 2  # Robberies and Armed Robberies
    elif "RAPE" in body_upper or "SEXUAL ASSAULT" in body_upper or "SEXUAL ABUSE" in body_upper:
        return 3  # Rape & Sexual Abuse
    elif "HOMICIDE" in body_upper or "MURDER" in body_upper or "KILLING" in body_upper:
        return 4  # Homicide
    elif "POLICE" in body_upper and "ACCIDENT" in body_upper:
        return 5  # Police Accidents
    elif "FATAL" in body_upper and "ACCIDENT" in body_upper:
        return 6  # Fatal Accidents
    
    # Fallback to section title hints
    if "THEFT" in title_upper and "HOUSE" not in title_upper:
        return 0
    elif "HOUSE" in title_upper or "BREAK" in title_upper:
        return 1
    elif "ROBBER" in title_upper:
        return 2
    elif "FATAL" in title_upper:
        return 6
    
    return 7  # Others


def _map_to_case_row(body: str) -> int:
    """
    Map incident to official 28-row case table index (0-27).
    Returns index into the 28-row case breakdown.
    """
    if not body:
        return 27  # Others (row 28)
    
    body_upper = body.upper()
    
    # Explicit keyword mapping to 28 official rows
    if any(x in body_upper for x in ["TERROR", "SUBVERSIVE", "EXPLOSIVE ATTACK"]):
        return 0  # Terrorist Activities
    elif any(x in body_upper for x in ["ARMS", "WEAPON", "PISTOL", "GRENADE", "MAOSER", "GUN", "AMMUNITION", "RIFLE"]):
        return 1  # Recovery of Arms & Ammunition
    elif any(x in body_upper for x in ["PROTEST", "STRIKE", "DEMONSTRATION", "UNREST", "RIOT"]):
        return 2  # Protests & Strikes
    elif any(x in body_upper for x in ["HOMICIDE", "MURDER", "KILLING", "FATAL ASSAULT", "DEATH BY STABBING"]):
        return 3  # Homicides
    elif "ROBBER" in body_upper or "ARMED ROBBERY" in body_upper or "SNATCH" in body_upper:
        return 4  # Robberies
    elif "VEHICLE" in body_upper and "THEFT" in body_upper:
        return 5  # Thefts of Vehicles
    elif "THEFT" in body_upper and "HOUSE" not in body_upper and "VEHICLE" not in body_upper:
        return 6  # Thefts of Properties
    elif any(x in body_upper for x in ["HOUSE BREAK", "BURGLARY", "HB &", "BREAKING AND ENTERING"]):
        return 7  # House Breaking & Theft
    elif any(x in body_upper for x in ["RAPE", "SEXUAL ASSAULT", "SEXUAL ABUSE", "CHILD ABUSE", "INDECENT"]):
        return 8  # Rape & Sexual Abuse
    elif "FATAL" in body_upper and "ACCIDENT" in body_upper:
        return 9  # Fatal Accidents
    elif any(x in body_upper for x in ["DEAD BODIES", "SUSPICIOUS DEAD", "FOUND DEAD", "UNIDENTIFIED BODY", "CORPSE"]):
        return 10  # Unidentified dead bodies
    elif "POLICE" in body_upper and "ACCIDENT" in body_upper:
        return 11  # Police Accidents
    elif any(x in body_upper for x in ["NARCOTIC", "HEROIN", "CANNABIS", "GANJA", "ICE", "METH", "COCAINE", "DRUG"]):
        return 18  # Detections of Narcotics
    elif any(x in body_upper for x in ["ILLEGAL LIQUOR", "KASIPPU", "ARRACK", "ILLICIT LIQUOR"]):
        return 18  # Detections of Illicit Liquor
    
    # Default to Others
    return 27


def _is_solved_incident(body: str) -> bool:
    """Check if incident appears to be solved based on keywords."""
    if not body:
        return False
    
    body_lower = body.lower()
    
    solved_keywords = [
        "arrest", "arrested", "apprehend", "apprehended", "remand", "remanded",
        "recover", "recovered", "seized", "confessed", "identified", "charged"
    ]
    
    return any(kw in body_lower for kw in solved_keywords)


