# GPU OCR Api (Surya) via Kaggle
# ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
def _surya_api_page(image: Image.Image) -> str:
    # ?????? LOCAL SAFETY GUARD: Aggressive Resize for Connection Stability ??????
    try:
        max_dim = 2000 # Lowered from 2500 for better socket handling
        w, h = image.size
        if max(w, h) > max_dim:
            scale = max_dim / max(w, h)
            new_size = (int(w * scale), int(h * scale))
            image = image.resize(new_size, Image.Resampling.LANCZOS)
            print(f"  [OCR] Resized page to {new_size[0]}x{new_size[1]} (Safe Connection Mode)")
    except Exception as e:
        print(f"  [OCR] Resize failed: {e}")

    try:
        # Convert PIL Image to bytes
        img_byte_arr = io.BytesIO()
        image.save(img_byte_arr, format='JPEG', quality=75) # Lowered for smaller upload payload
        img_byte_arr.seek(0)

        # Send to API via persistent session
        files = {'file': ('page.jpg', img_byte_arr, 'image/jpeg')}
        headers = {"ngrok-skip-browser-warning": "true"}

        # We use the session with built-in retries
        response = _session.post(KAGGLE_OCR_URL, files=files, headers=headers, timeout=300)

        if response.status_code == 200:
            data = response.json()
            if data.get("success"):
                return data.get("text", "")
            else:
                error_msg = data.get('error', 'Unknown Error')
                print(f"[Surya API] Server Error: {error_msg}")
        elif response.status_code == 500:
            try:
                srv_data = response.json()
                err = srv_data.get("error", "Internal Server Error")
                print(f"[Surya API] ?????? Kaggle Crash: {err}")
            except Exception:
                print("[Surya API] ?????? HTTP 500: Check Kaggle console.")
        else:
            print(f"[Surya API] HTTP Error: {response.status_code}")
    except Exception as e:
        # Handle the specific 10053 error with a helpful message
        if "10053" in str(e):
            print("[Surya API] ??????? Connection aborted by local PC (Error 10053). Retrying sequentially...")
        else:
            print(f"[Surya API] Request failed: {e}")

    return ""

def extract_text_surya_api(images: list, progress_callback=None) -> list:
    total = len(images)
    results = [""] * total

    def process(args):
        idx, img = args
        if progress_callback:
            progress_callback(f"Surya (GPU): page {idx + 1}/{total}")
        return idx, _surya_api_page(img)

    with ThreadPoolExecutor(max_workers=MAX_WORKERS) as pool:
        futures = {pool.submit(process, (i, img)): i for i, img in enumerate(images)}
        for future in as_completed(futures):
            idx, text = future.result()
            results[idx] = text
            # Throttle: Brief rest to allow Kaggle GPU to clear cache/context
            if MAX_WORKERS == 1:
                time.sleep(1.2)

    return results

