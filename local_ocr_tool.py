def extract_text_from_pdf(pdf_path: str, language: str = "sin",
                           progress_callback=None) -> list:
    """
    Extract Sinhala text from a PDF file.
    Tries Surya AI first; falls back to Tesseract on failure.
    Returns a list of page-text strings.
    """
    if not os.path.exists(pdf_path):
        raise FileNotFoundError(pdf_path)

    images = _pdf_to_images(pdf_path)

    # ?????? Surya (GPU) Engine First ???????????????????????????????????????
    print(f"[OCR] Trying GPU Fast API: {KAGGLE_OCR_URL}")
    gpu_results = extract_text_surya_api(images, progress_callback=progress_callback)

    # Check if GPU OCR actually succeeded (meaning at least some text was extracted)
    if any(len(p.strip()) > 10 for p in gpu_results):
        print("[OCR] ??? Successfully extracted via Kaggle GPU!")
        return gpu_results

    # ?????? Fallback Tesseract Engine ???????????????????????????????????????
    print("[OCR] ?????? GPU API failed or returned empty. Falling back to Local Tesseract.")
    return extract_text_tesseract(images, lang=language,
                                   progress_callback=progress_callback)


def extract_text_from_image(image_path: str, language: str = "sin") -> str:
    """
    Extract Sinhala text from a single image file.
    Returns a single string.
    """
    if not os.path.exists(image_path):
        raise FileNotFoundError(image_path)

    image = Image.open(image_path)

    # ?????? Surya (GPU) Engine First ???????????????????????????????????????
    print(f"[OCR] Trying GPU Fast API for image: {KAGGLE_OCR_URL}")
    api_text = _surya_api_page(image)
    if len(api_text.strip()) > 10:
        return api_text

    print("[OCR] ?????? GPU API failed. Falling back to Local Tesseract.")
    # ?????? Fallback Tesseract Engine ???????????????????????????????????????
    pytesseract = _get_pytesseract()
    return _tesseract_page(pytesseract, image, language)


